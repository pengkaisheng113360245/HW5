#include <stdio.h>
#include <stdlib.h>

#define SIZE 10 // 定義陣列大小

// 函式宣告
void bubbleSort(int* const array, const int size);
void swap(int* element1Ptr, int* element2Ptr);

int main(void) {
    // 初始化陣列
    int a[SIZE] = { 2, 6, 4, 8, 10, 12, 89, 68, 45, 37 };
    int i;

    // 顯示原始資料
    printf("Data items in original order\n");
    for (i = 0; i < SIZE; i++) {
        printf("%4d", a[i]);
    }

    // 呼叫 bubbleSort 函式進行排序
    bubbleSort(a, SIZE);

    // 顯示排序後的資料
    printf("\nData items in ascending order\n");
    for (i = 0; i < SIZE; i++) {
        printf("%4d", a[i]);
    }

    printf("\n");
    system("pause"); // 等待使用者按鍵繼續
    return 0;
}

// 氣泡排序函式
void bubbleSort(int* const array, const int size) {
    void swap(int* element1Ptr, int* element2Ptr); // 函式原型
    int pass;
    int j;

    // 氣泡排序演算法
    for (pass = 0; pass < size - 1; pass++) {
        for (j = 0; j < size - 1; j++) {
            if (array[j] > array[j + 1]) {
                swap(&array[j], &array[j + 1]); // 交換元素
            }
        }
    }
}

// 交換函式
void swap(int* element1Ptr, int* element2Ptr) {
    int hold = *element1Ptr;
    *element1Ptr = *element2Ptr;
    *element2Ptr = hold;
}
